CREATE TABLE NHANVIEN
(
    MANV char(4) CONSTRAINT PK_NV PRIMARY KEY,
	HOTEN varchar(40),
	SODT varchar(20),
	NGVL smalldatetime
)

CREATE TABLE SANPHAM
(
    MASP char(4) CONSTRAINT PK_SP PRIMARY KEY,
	TENSP varchar(40),
	DVT varchar(20),
	NUOCSX varchar(40),
	GIA money
)

CREATE TABLE HOADON
(
    SOHD int CONSTRAINT PK_HD PRIMARY KEY,
	NGHD smalldatetime,
	MAKH char(4),
	MANV char(4),
	TRIGIA money
)

CREATE TABLE CTHD
(
    SOHD int,
	MASP char(4),
	SL int
	CONSTRAINT PK_CTHD PRIMARY KEY(SOHD,MASP)
)
SET DATEFORMAT dmy

